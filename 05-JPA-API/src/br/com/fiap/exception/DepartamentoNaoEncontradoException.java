package br.com.fiap.exception;

public class DepartamentoNaoEncontradoException extends Exception {

	public DepartamentoNaoEncontradoException() {
		super();
	}

	public DepartamentoNaoEncontradoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public DepartamentoNaoEncontradoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	
	}

	public DepartamentoNaoEncontradoException(String arg0) {
		super(arg0);
		
	}

	public DepartamentoNaoEncontradoException(Throwable arg0) {
		super(arg0);
		
	}
	
	
	

}
